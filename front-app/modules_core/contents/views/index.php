        <h3 class="proTitle">Recent Changes and the Reasoning Behind Them</h3>
        <p>Lawmakers create new traffic laws and make changes to existing laws to make sure the laws stay up-to-date. Changes in vehicles, technology, economics, driving habits, and social norms require changes in laws. Drivers are responsible for knowing and obeying all the traffic laws currently in effect; ignorance is no excuse for not following the laws.</p>
        <p>1. Changes to California Traffic Law: New and Amended Laws</p>
        <p>Child Passenger Safety Seat Law (effective January 1, 2012)</p>
        <p>Vehicle Code Section 27363</p>
        <h3 class="proTitle">What does the new law say?</h3>
        <p>Any child who is under the age of eight and who is less than 4 feet 9 inches tall must be secured in a federally-approved child passenger restraint system and must ride in the back seat of the vehicle.</p>
        <p>A federally-approved child passenger restraint system can be a child car seat that fits the childâ€™s current size.</p>
        <p>However, children under eight years of age who are 4 feet 9 inches tall or taller may be secured with an appropriate safety belt instead of a child safety car seat.</p>
        <h3 class="proTitle">All children under eight years old who are less than 4 feet 9 inches tall may ride in the front seat of a vehicle only in the following instances: </h3>
        <ul>
          <li>There is no back seat in the vehicle.</li>
          <li>The back seats are rear-facing seats or side-facing jump seats.</li>
          <li>It is not possible to install the restraint system properly in the back seat.</li>
          <li>Children seven years of age or under are already in all the back seats.</li>
          <li>The child has to ride in the front seat for medical reasons.</li>
        </ul>
        <h3 class="proTitle">Unchanged is the regulation that a child may not ride in the front seat if there is an active passenger airbag if the child</h3>
        <ul>
          <li>Is under one year old.</li>
          <li>Weights less than 20 pounds.</li>
          <li>Is riding in a rear-facing child safety seat.</li>
        </ul>
        <h3 class="proTitle">Why was the law changed?</h3>
        <p>Young children are at much greater risk for injury in case of a collision when they ride in the front seat of a vehicle. When you move a child from the front seat to the back seat, the child is one-third less likely to get injured.</p>
