            <h4>Example scenarious</h4>
            <div>
              <p>As an example of how to identify and respond to a developing hazard, consider a parked vehicle on the side of the road. When you first see it, it is not doing anything; it is just a parked vehicle. If you were to respond to the vehicle at this point, you would not score any marks, but you would not lose any marks. 
      
      However, when you get closer to the vehicle, you notice that the car's right hand indicator starts to flash. The indicator would lead you to believe that the driver of the vehicle has an intention of moving away, therefore the hazard is now developing and a response at this point would score marks. The indicator coming on is a sign that the parked vehicle has changed its status from a potential hazard into a developing hazard. </p>
