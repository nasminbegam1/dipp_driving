            <h4 class="open">How to use</h4>
            <div>
              <p>Each of the hazard perception videos contain at least one developing hazard. A developing hazard is one which will eventually cause you to change course, speed or even to stop.Each time you think you spot such a hazard developing in the video, click anywhere on the video. Every time you click the video, a red flag is shown under the video.Each hazard carries a maximum score of 5 points, the earlier you spot the hazard the higher your score will be. As the hazard 
      develops, the maximum you can score decreases until the hazard has passed at which point you will score zero.You will not be able to review your answers to the hazard perception test; as on the road, you will only have one chance to respond to the developing hazard.
      Do not get into the habit of repeatedly clicking the mouse button as in the real DVSA test you will be penalised for this and score zero marks for the video.Spotting the developing hazards early on takes some practice so don't be discouraged if at first you score low marks.
      Above all, remember to concentrate and stay alert.</p>
